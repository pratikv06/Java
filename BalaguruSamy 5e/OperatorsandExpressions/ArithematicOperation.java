class ArithematicOperation{
   public static void main (String args[]){
      float a= 20.5F, b= 6.4F;
      System.out.println("a = " +a);
      System.out.println("b = " +b);
      System.out.println("a + b = " + (a+b));
      System.out.println("a - b = " + (a-b));
      System.out.println("a * b = " + (a*b));
      System.out.println("a / b = " + (a/b));
      System.out.println("a % b = " + (a%b));
   }
} 