class MathFunction{
   public static void main (String a[]){
      System.out.println("The maximum among 22 and 33 is " + Math.max(22,33));
      System.out.println("The square root of 64 is " + Math.sqrt(64));
      System.out.println("The absolute value of -55 is " + Math.abs(-55));
      System.out.println("The cube of 16 is equal to " + Math.pow(16,3));
      System.out.println("The minimum among 22 and 33 is " + Math.min(22,33));
      System.out.println("The natural log of 3 is " + Math.log(3));
      System.out.println("\n ** Some Extra Function: **" );
      System.out.println("The floor value of 23.23 is " + Math.floor(23.23));
      System.out.println("The ceil value of 23.23 is " + Math.ceil(23.23));
      System.out.println("The Sin(60) is " + Math.sin(60));
      System.out.println("The Cos(30) is " + Math.cos(30));
      System.out.println("The Tan(45) is " + Math.tan(45));
   }
}