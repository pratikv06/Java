import java.lang.Math;

class Squareroot{
   public static void main(String args[]){
      double x=5; //Floating Point Number Declaration and Initalization
      double y; // Simple Declaration
      y = Math.sqrt(x);
      System.out.println("y = " + y); // Here + is act as concatetaion
   }
}