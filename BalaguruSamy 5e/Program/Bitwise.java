/**
 * Program:
 * Demonstrate of bitwise operators
 */

class Bitwise{
   public static void main(String[] args) {
      int a = 13, b = 25;
      System.out.println(":: Value ::");
      System.out.println("a = " +a);
      System.out.println("b = " +b);
      System.out.println(":: Bitwise Operator ::");
      System.out.println("a & b = " +(a&b));
      System.out.println("a | b = " +(a|b));
      System.out.println("a ^ b = " +(a^b));
      
   }
}